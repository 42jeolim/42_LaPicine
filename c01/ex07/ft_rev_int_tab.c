void	ft_rev_int_tab(int *tab, int size)
{
	int	tmp[100000];
	int	i;
	int	x;

	x = 0;
	i = size - 1;
	while (i >= 0)
	{
		tmp[x] = tab[i];
		x++;
		i--;
	}
	x = 0;
	while (x < size)
	{
		tab[x] = tmp[x];
		x++;
	}
}
